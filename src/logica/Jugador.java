package logica;

/**
 *
 * @author coder
 */
public class Jugador {
    private int id;
    private String nombre;
    private double tiempo;
    private int jugadas;

    public Jugador(int id, String nombre, double tiempo, int jugadas) {
        this.id = id;
        this.nombre = nombre;
        this.tiempo = 0.0;
        this.jugadas = 0;
    }
    
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getTiempo() {
        return tiempo;
    }

    public void setTiempo(double tiempo) {
        this.tiempo = tiempo;
    }

    public int getJugadas() {
        return jugadas;
    }

    public void setJugadas(int jugadas) {
        this.jugadas = jugadas;
    }
    
}
